# 为学校人事部门设计一个简单的人事管理程序，满足如下要求：
# （1）学校人员分为三类：教师、学生、职员；
# （2）三类人员的共同属性是姓名、性别、年龄、部门；
# （3）教师的特别属性是职称、主讲课程；
# （4）学生的特别属性是专业、入学日期；
# （5）职员的特别属性是工资。
# 建议使用继承提升代码复用度，使用类属性统计学校总人数和各类人员的人数，能够随着新人进入注册和离校注销而动态变化。

